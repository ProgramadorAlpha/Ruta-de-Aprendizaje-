//Bucles es una estrucutura de datos que se va repitiendo un numero de veces definido Aqui tenemos el "FOR" EJEMPLO:

/* 
Algunos sinónimos para "bucle" en programación son:

Repetir
Ciclo
Iteración
Vuelta
Loop

Todos estos términos se refieren a una estructura de control de flujo en la que se ejecuta un bloque de código varias veces. En la programación, los bucles 
se utilizan a menudo para realizar tareas repetidas o para procesar un conjunto de datos.

Por ejemplo, podrías utilizar un bucle para recorrer todos los elementos de una matriz y realizar una tarea específica en cada uno de ellos, o podrías utilizar un bucle 
para repetir una tarea hasta que se cumpla una condición específica.



*/

for (var numero = 1; numero <= 100; numero++) {
    console.log("Ahora vamos por el número: " + numero);
  }
  


